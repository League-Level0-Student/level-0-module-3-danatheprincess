package _10_99_bottles;

public class _99Bottles {
public static void main(String[] args) {
	for(int b=99;  b>1;b-- ) {
		System.out.println(b+ " bottles of beer on the wall, " +b+ "  bottles of beer.");
		System.out.println("Take one down and pass it around, " +(b-1) + " bottles of beer on the wall.");

	}
	
	System.out.println("1 bottles of beer on the wall, 1 bottles of beer.");
	System.out.println("Take one down and pass it around, no more bottles of beer on the wall.");
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
